class TrapezoidArea
{
public static void main(String []args)
{
int h,parallel_side_a,parallel_side_b;
double area;
h=10;		//vertical height
parallel_side_a=5;
parallel_side_b=8;
area=0.5*(parallel_side_a+parallel_side_b)*h;
System.out.println("Trapezoid parallel sides are "+parallel_side_a+", "+parallel_side_b+" and height is "+h+" Then the area of Trapezoid is "+area);
}
}